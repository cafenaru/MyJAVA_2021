package icehs.science.chapter04;

import java.util.Scanner;

public class CaculatorTest {
	public static void main(String[] args) {
		
		int firstNum = 20;
		int secondNum = 7;
		
		int addResult = firstNum + secondNum;
		int substractResult = firstNum - secondNum;
		int mulResult = firstNum *  secondNum;
		int divResult = firstNum / secondNum;
		int remainResult = firstNum % secondNum;
		
		System.out.println("오챯叩 : " + addResult);
		System.out.println("짋챯叩 : " + substractResult);
		System.out.println("썼챯叩 : " + mulResult);
		System.out.println("씱얋챯叩 : "  + divResult);
		System.out.println("씱쟵쵔 " + remainResult);
		 
	}
}
